"""
1. Simple Counter:
Write a program that uses a while loop to print numbers from 1 to 10.
"""
x = 1
while x <= 10:
    print(x)
    x += 1
# I made x = 1 and made a while loop to make x less than or equal to 10 since we want it to print from 1 to 10. Then I added 1 everytime to x until it got to 10.
